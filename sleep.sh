#!/bin/bash  
  
# lets begin the task  
date +"%H:%M:%S"  
  
echo " you have to wait for 10 seconds"  
  
# sleep for 9 seconds  
sleep 10s   
# you can also use "sleep 9" in place of "sleep 9s" because if there is no suffix, it is considered as "seconds".  
  
  
# end time  
date +"%H:%M:%S"  
  
echo "Your Task just got Completed"  